var title = document.getElementById("icon-title");
var icon_skill = document.getElementById("icon-skill");
var nivel_skill = document.getElementById("nivel-skill");
var inf_skill = document.getElementById("inf-skill");
var container_icon_skill = document.getElementById("toolskill");

//adicionar os icones no conatener icons
iconsData.forEach((element) => {
  fill_icons(element.url, element.alt, element.id);
});

function fill_icons(src, alt, id) {
  let row_data = document.createElement("img");
  row_data.src = src;
  row_data.alt = alt;
  row_data.id = id;
  row_data.addEventListener("click", function () {
    loadskill(this.id);
  });
  row_data.classList.add("icon_skill_container");
  container_icon_skill.appendChild(row_data);
}

//Exibir detalhe da skill
loadskill(1);
function loadskill(id) {
  iconsData.forEach((element) => {
    if (Number(element.id) === Number(id)) {
      title.innerHTML = element.title;
      icon_skill.src = element.url;
      icon_skill.alt = element.title;
      nivel_skill.innerHTML = element.conhecimento;
      inf_skill.innerHTML = element.texto;
      return true;
    }
  });
}
